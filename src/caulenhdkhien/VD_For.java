package caulenhdkhien;

public class VD_For {
    public static void main(String[] args) {
        // in ra bang cuu chuong
        for (int i =2; i<=9;i++){
            for (int j = 1;j<=10;j++){
                System.out.println(i+"x"+j+"="+(i*j));
            }
            System.out.println("----");

        }    }
}
